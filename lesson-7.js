//Exercício sete: Fazer um programa que determine se um número é positivo, negativo ou zero.

let a = 0;

console.log("O número sendo utilizado atualmente é: " + a);

if(a < 0){
    console.log("O número é negativo!");
}else if(a > 0){
    console.log("O número é positivo!")
}else{
    console.log("O número é zero!")
}
